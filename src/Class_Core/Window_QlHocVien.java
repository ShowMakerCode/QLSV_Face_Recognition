    private void LoadDataToCBB_ChuyenDe() {
        model_cbbCD.removeAllElements();
        try {
            listcd = dao_cd.SelectAll();
            for (int i = 0; i < listcd.size(); i++) {
                ChuyenDe get = listcd.get(i);
                model_cbbCD.addElement(get);
            }
        } catch (Exception ex) {
            Logger.getLogger(Window_QlHocVien.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void LoadDataToCBB_KhoaHoc() {
        cbbKhoaHoc.removeAllItems();
        String sql = "select * from KhoaHoc\n"
                + "  where MaCD = ?";
        try {
            ChuyenDe cdx = new ChuyenDe();
            cdx = (ChuyenDe) model_cbbCD.getSelectedItem();
            PreparedStatement pt = con.prepareStatement(sql);
            pt.setString(1, cdx.getMaCD());
            ResultSet rs = pt.executeQuery();
            while (rs.next()) {
                KhoaHoc kha = new KhoaHoc(rs.getString(2), rs.getInt(4),
                        rs.getFloat(3), rs.getString(5), rs.getString(6),
                        rs.getString(7), rs.getString(8), rs.getInt(1));
                model_cbbKH.addElement(kha);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void LoadDataToTableHocVien() {
        modeltbl_HV.setRowCount(0);
        String sql = "select MaHV,HocVien.MaNH,HoTen,Diem "
                + "from HocVien join NguoiHoc on HocVien.MaNH = NguoiHoc.MaNH\n"
                + "  where MaKH = ?";
        try {
            int i = 0;
            KhoaHoc khhv = (KhoaHoc) model_cbbKH.getSelectedItem();
            if (khhv == null) {
                return;
            }
            PreparedStatement pt = con.prepareStatement(sql);
            pt.setInt(1, khhv.getMaKH());
            ResultSet rs = pt.executeQuery();
            while (rs.next()) {
                i++;
                Object[] rowData = new Object[]{
                    i,
                    rs.getInt(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getFloat(4)
                };
                modeltbl_HV.addRow(rowData);
            }
            LoadDataToTableNguoiHoc();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void LoadDataToTableNguoiHoc() {
        String sql = "SELECT MaNH, HoTen, GioiTinh,"
                + " convert(varchar, GETDATE(), 103) NgaySinh, "
                + "DienThoai, Email,convert(varchar, GETDATE(), 103)NgayDK, MaNV, GhiChu\n"
                + "from NguoiHoc where MaNH not in (select MaNH from HocVien where MaKH = ?)";
        try {
            modeltbl_NH.setRowCount(0);
            PreparedStatement pt = con.prepareStatement(sql);
            pt.setInt(1, Integer.parseInt(String.valueOf(cbbKhoaHoc.getSelectedItem())));
            ResultSet rs = pt.executeQuery();
            while (rs.next()) {
                String gt = rs.getBoolean(3) ? "Nam" : "Nữ";
                Object[] rowData = new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    gt,
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6)
                };
                modeltbl_NH.addRow(rowData);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

