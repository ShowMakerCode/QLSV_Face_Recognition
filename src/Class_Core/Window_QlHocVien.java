    private void btncapnhatdiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncapnhatdiemActionPerformed
        try {
            if (tblHocVienm.getSelectedRow() < 0) {
                return;
            }
            for (int i = 0; i < tblHocVienm.getRowCount(); i++) {
                if (Float.parseFloat(String.valueOf(tblHocVienm.getValueAt(i, 4))) > 10
                        | Float.parseFloat(String.valueOf(tblHocVienm.getValueAt(i, 4))) < 0) {
                    LoadDataToTableHocVien();
                    MyMessage.msgFalse("Điểm Phải Trên 0 và Nhỏ Hơn 10");
                    return;
                }
                if (dao_hv.Update(new HocVien(
                        Integer.parseInt(String.valueOf(tblHocVienm.getValueAt(i, 1))), // MãHV
                        Integer.parseInt(String.valueOf(cbbKhoaHoc.getSelectedItem())), // Mã KH
                        String.valueOf(tblHocVienm.getValueAt(i, 2)),// mã NH
                        Float.parseFloat(String.valueOf(tblHocVienm.getValueAt(i, 4)))))); //Điểm
            }
            MyMessage.msgTrue("Sửa Thành Công");
            LoadDataToTableHocVien();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btncapnhatdiemActionPerformed

    private void btnxoakhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoakhActionPerformed
        int vitri = tblHocVienm.getSelectedRow();
        if (vitri < 0) {
            return;
        }
        try {
            if (dao_hv.Delete(String.valueOf(tblHocVienm.getValueAt(vitri, 1)))) {
                MyMessage.msgTrue("Xóa Thành Công");
                LoadDataToTableHocVien();
            };
        } catch (Exception ex) {
            Logger.getLogger(Window_QlHocVien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnxoakhActionPerformed

    private void btnthemvaokhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemvaokhActionPerformed

        try {
            int vitri = tblnguoihoc.getSelectedRow();
            if (vitri < 0) {
                return;
            }
            if (cbbKhoaHoc.getSelectedItem() == null) {
                MyMessage.msgWarning("Không Có Mã Khóa Học");
                return;
            }

            if (dao_hv.Insert(new HocVien(Integer.parseInt(String.valueOf(cbbKhoaHoc.getSelectedItem())),
                    String.valueOf(tblnguoihoc.getValueAt(vitri, 0)),
                    0))) {
                MyMessage.msgTrue("Thêm Thành Công");
                LoadDataToTableHocVien();
            };

        } catch (Exception ex) {
            ex.printStackTrace();
        }


    }//GEN-LAST:event_btnthemvaokhActionPerformed

